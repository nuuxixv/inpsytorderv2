-- 상태: 설계 검토용 초안. CTO 검수 + 건우님 승인 전 SQL Editor 실행 금지.
-- 근거: DOCS/PRD_검사위계.md §6 데이터모델 / §7 (절판·판매중지 = is_active=false 미노출).
--
-- 왜 검사 위계 컬럼과 별도 파일인가:
--   is_active 는 검사 위계 전용이 아니라 "상품 전체 공통" 노출여부 필드.
--   절판·판매중지(K-BASC-2·K-Bayley-Ⅲ·K-WAIS-IV 등)를 삭제하지 않고 숨김 처리
--   (재판매·타팀 이관 대비). 검사군 컬럼과 라이프사이클이 달라 파일을 분리함.
--   (검사군 컬럼 파일을 롤백해도 상품 노출여부 정책은 독립적으로 유지 가능.)
--
-- 기본값 true 인 이유:
--   기존 전 상품이 현재 노출 중 = 마이그레이션 시 모두 true 로 채워져 진열 회귀 없음.
--   숨길 상품만 운영자가 개별 false 로 토글(어드민 UI, §7 오픈 이슈).
--
-- order_items / 매출 집계 무영향:
--   is_active 는 진열 필터 전용. 주문 스냅샷·매출 집계(category 기반) 경로 미참조.
--
-- 가법·멱등·RLS/GRANT:
--   - ADD COLUMN IF NOT EXISTS 1개 → 재실행 안전(멱등). 기존 컬럼·데이터 무변경.
--   - products RLS 정책·GRANT 무변경(신규 컬럼은 기존 정책·GRANT 상속).
--
-- ---------------------------------------------------------------------
-- [적용] Supabase 대시보드 → SQL Editor → (검수·승인 후) 본 파일 전문 1회 실행.
-- ---------------------------------------------------------------------
--
-- [롤백] (원복 필요 시 — 데이터 손실 주의)
--   ALTER TABLE public.products DROP COLUMN IF EXISTS is_active;
-- =====================================================================

ALTER TABLE public.products
    ADD COLUMN IF NOT EXISTS is_active boolean NOT NULL DEFAULT true;

COMMENT ON COLUMN public.products.is_active IS '상품 전역 노출여부. true=진열(기본), false=미노출(절판·판매중지). 삭제 대신 숨김 — 재판매·이관 대비. 진열 필터 전용, 매출 집계·order_items 무관.';

